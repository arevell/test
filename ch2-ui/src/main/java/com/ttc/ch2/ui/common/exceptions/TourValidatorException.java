package com.ttc.ch2.ui.common.exceptions;

public class TourValidatorException extends RuntimeException {

	public TourValidatorException() {
		super();
	}

	public TourValidatorException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public TourValidatorException(String arg0) {
		super(arg0);
	}

	public TourValidatorException(Throwable arg0) {
		super(arg0);
	}

}
